def expand_aspects(aspect_list):
    expanded = set(aspect_list)
    if not USE_WORDNET_EXPANSION:
        return expanded
    for aspect in aspect_list:
        syns = [l.name().replace('_',' ').lower() for s in wn.synsets(aspect) for l in s.lemmas()]
        syns = [s for s in syns if s != aspect][:MAX_SYNONYMS]
        expanded.update(syns)
    return expanded

try:
    base_aspects = CATEGORY_ASPECTS[PRODUCT_CATEGORY.upper()]
except KeyError:
    print(f"⚠️ Unknown category '{PRODUCT_CATEGORY}'. Defaulting to CLOTHING.")
    base_aspects = CATEGORY_ASPECTS["CLOTHING"]

expanded_aspects = expand_aspects(base_aspects)

def extract_aspects(text):
    text = str(text).lower()
    return [a for a in expanded_aspects if re.search(rf"\b{re.escape(a)}\b", text)]

df["aspects"] = df["clean_text"].apply(extract_aspects)
