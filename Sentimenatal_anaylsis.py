def batch_predict_with_confidence(texts, pipe, batch_size=32, neutral_margin=NEUTRAL_MARGIN):
    """Get both labels and confidence in one pass (batched)"""
    labels = []
    confidences = []

    for i in tqdm(range(0, len(texts), batch_size), desc="Analyzing sentiment"):
        batch = texts[i:i+batch_size]
        batch = [str(x) for x in batch]  # ensure all inputs are strings
        batch_results = pipe(batch, truncation=True, max_length=512)

        for result in batch_results:
            score = result.get('score', 0.0)
            label = result.get('label', '').upper()
            confidences.append(score)

            # Normalize label names (different models use different naming)
            if 'POSITIVE' in label or 'POS' in label or label == 'LABEL_2':
                label = 'POSITIVE'
            elif 'NEGATIVE' in label or 'NEG' in label or label == 'LABEL_0':
                label = 'NEGATIVE'
            elif 'NEUTRAL' in label or 'NEU' in label or label == 'LABEL_1':
                label = 'NEUTRAL'

            # For models without native neutral, use threshold
            if label in ['POSITIVE', 'NEGATIVE'] and (0.4 < score < 0.6 or score < neutral_margin):
                labels.append("NEUTRAL")
            else:
                labels.append(label)

    return labels, confidences

# Execute sentiment analysis
